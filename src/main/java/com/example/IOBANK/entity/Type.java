package com.example.IOBANK.entity;

public enum Type {
    /**
     * Represents a withdrawal transaction.
     */
    WITHDRAW,

    /**
     * Represents a deposit transaction.
     */
    DEPOSIT,

    /**
     * Represents a debit transaction.
     */
    DEBIT,

    /**
     * Represents a conversion transaction.
     */
    CONVERSION,

    /**
     * Represents a credit transaction.
     */
    CREDIT
}
